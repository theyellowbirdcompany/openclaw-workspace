#!/usr/bin/env node
/**
 * Self-Skill-Writer — Autonomous Rule Generation
 * 
 * Detects repeated violations in MEMORY.md and auto-generates permanent skills.
 * 
 * Usage:
 *   node self-skill-writer.js --detect    # Scan for patterns
 *   node self-skill-writer.js --generate  # Create skill from detected pattern
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const MEMORY_PATH = '/home/clawd/.openclaw/workspaces/Devan/MEMORY.md';
const SKILLS_DIR = '/home/clawd/.openclaw/workspaces/Devan/.agents/skills';
const DETECTION_THRESHOLD = 3; // Pattern must appear 3+ times

// Pattern signatures to detect
const VIOLATION_PATTERNS = [
  {
    signature: /Inter|Roboto/gi,
    category: 'typography',
    violation: 'Forbidden font family',
    rule: 'Never use Inter or Roboto — Space Mono (headings) + IBM Plex Sans (body) only'
  },
  {
    signature: /purple.*gradient|gradient.*purple/gi,
    category: 'color',
    violation: 'Purple gradient usage',
    rule: 'Purple-to-blue gradients are banned — use Navy/Gold palette'
  },
  {
    signature: /text-align:\s*center|centered.*body|center.*align.*body/gi,
    category: 'layout',
    violation: 'Center-aligned body text',
    rule: 'Never center-align body copy — left-align for readability'
  },
  {
    signature: /border-radius:\s*(9|[1-9]\d+)px/gi,
    category: 'styling',
    violation: 'Excessive border radius',
    rule: 'Max 8px border-radius — sharp corners preferred'
  }
];

/**
 * Scan MEMORY.md for repeated violation patterns
 */
function detectPatterns() {
  if (!fs.existsSync(MEMORY_PATH)) {
    console.log('❌ MEMORY.md not found');
    return [];
  }

  const memory = fs.readFileSync(MEMORY_PATH, 'utf-8');
  const detections = [];

  for (const pattern of VIOLATION_PATTERNS) {
    const matches = memory.match(pattern.signature) || [];
    
    if (matches.length >= DETECTION_THRESHOLD) {
      detections.push({
        ...pattern,
        occurrences: matches.length,
        status: 'NEEDS_SKILL'
      });
    }
  }

  return detections;
}

/**
 * Check if a skill already exists for this violation
 */
function skillExists(category, violation) {
  const skillName = `auto-${category}-${violation.toLowerCase().replace(/\s+/g, '-')}`;
  const skillPath = path.join(SKILLS_DIR, skillName, 'SKILL.md');
  return fs.existsSync(skillPath);
}

/**
 * Generate SKILL.md content from violation pattern
 */
function generateSkillContent(pattern) {
  const { category, violation, rule, signature } = pattern;
  
  return `# Auto-Generated Skill: ${violation}

**Category:** ${category}
**Generated:** ${new Date().toISOString()}
**Trigger:** Pattern detected ${pattern.occurrences}x in MEMORY.md

---

## Violation Pattern

\`\`\`
${signature.source}
\`\`\`

---

## Rule

${rule}

---

## Auto-Enforcement

This skill was automatically generated by the self-skill-writer after detecting repeated violations.

When triggered during design work:
1. Scan code/CSS for violation pattern
2. Alert agent immediately
3. Prevent shipping if violation detected
4. Log to MEMORY.md for pattern tracking

---

## Integration

Add to Devan's AGENTS.md under "Auto-Generated Rules":

\`\`\`markdown
### ${violation}
- **Trigger:** ${signature.source}
- **Action:** Block and alert
- **Skill:** auto-${category}-${violation.toLowerCase().replace(/\s+/g, '-')}
\`\`\`

---

## Usage

This skill should be loaded automatically when Devan starts a design session.

To manually invoke: Reference this skill in pre-flight checks.

---

## Maintenance

If this rule changes or becomes obsolete:
1. Update SOUL.md with new conviction
2. Archive this skill (move to \`.archived/\`)
3. Create new auto-generated skill if needed

Self-skill-writer will detect the change and regenerate.
`;
}

/**
 * Create skill directory and SKILL.md
 */
function createSkill(pattern) {
  const { category, violation } = pattern;
  const skillName = `auto-${category}-${violation.toLowerCase().replace(/\s+/g, '-')}`;
  const skillDir = path.join(SKILLS_DIR, skillName);
  const skillPath = path.join(skillDir, 'SKILL.md');

  // Create directory
  if (!fs.existsSync(skillDir)) {
    fs.mkdirSync(skillDir, { recursive: true });
  }

  // Write SKILL.md
  const content = generateSkillContent(pattern);
  fs.writeFileSync(skillPath, content);

  console.log(`✅ Created skill: ${skillName}`);
  console.log(`   Path: ${skillPath}`);
  
  return { skillName, skillPath };
}

/**
 * Update Devan's AGENTS.md to register new skill
 */
function registerSkill(skillName, pattern) {
  const agentsPath = '/home/clawd/.openclaw/workspaces/Devan/AGENTS.md';
  
  if (!fs.existsSync(agentsPath)) {
    console.log('⚠️  AGENTS.md not found — skill created but not registered');
    return;
  }

  let agents = fs.readFileSync(agentsPath, 'utf-8');

  // Check if auto-generated rules section exists
  if (!agents.includes('## Auto-Generated Rules')) {
    agents += `\n\n---\n\n## Auto-Generated Rules\n\nSkills automatically created by self-skill-writer when patterns are detected 3+ times.\n\n`;
  }

  // Add skill entry
  const entry = `\n### ${pattern.violation}\n- **Trigger:** \`${pattern.signature.source}\`\n- **Action:** Block and alert\n- **Skill:** ${skillName}\n- **Generated:** ${new Date().toISOString()}\n`;

  agents += entry;
  fs.writeFileSync(agentsPath, agents);

  console.log(`✅ Registered skill in AGENTS.md`);
}

/**
 * Main CLI
 */
function main() {
  const args = process.argv.slice(2);
  const action = args[0];

  if (action === '--detect') {
    console.log('🔍 Scanning MEMORY.md for repeated violation patterns...\n');
    
    const detections = detectPatterns();
    
    if (detections.length === 0) {
      console.log('✅ No repeated violations detected (threshold: 3+ occurrences)');
      return;
    }

    console.log(`⚠️  Found ${detections.length} pattern(s) above threshold:\n`);
    
    detections.forEach((d, i) => {
      console.log(`${i + 1}. ${d.violation}`);
      console.log(`   Category: ${d.category}`);
      console.log(`   Occurrences: ${d.occurrences}`);
      console.log(`   Pattern: ${d.signature.source}`);
      console.log(`   Skill exists: ${skillExists(d.category, d.violation) ? '✅ Yes' : '❌ No'}`);
      console.log('');
    });

    console.log('\nRun with --generate to create skills for new violations.');
    
  } else if (action === '--generate') {
    console.log('🤖 Auto-generating skills from detected patterns...\n');
    
    const detections = detectPatterns();
    let created = 0;

    detections.forEach(pattern => {
      if (!skillExists(pattern.category, pattern.violation)) {
        const { skillName, skillPath } = createSkill(pattern);
        registerSkill(skillName, pattern);
        created++;
      } else {
        console.log(`⏭️  Skipped: ${pattern.violation} (skill already exists)`);
      }
    });

    if (created > 0) {
      console.log(`\n✅ Created ${created} new skill(s)`);
      console.log('   Skills are now active in Devan\'s workspace');
    } else {
      console.log('\n✅ All detected patterns already have skills');
    }

  } else {
    console.log('Usage:');
    console.log('  node self-skill-writer.js --detect    # Scan for patterns');
    console.log('  node self-skill-writer.js --generate  # Create skills');
  }
}

main();
